
# Calculator class to perform operations
class Calculator:
    def __init__(self):
        self.result = None

    def add(self, x, y):
        self.result = x + y

    def subtract(self, x, y):
        self.result = x - y

    def multiply(self, x, y):
        self.result = x * y

    def divide(self, x, y):
        self.result = x / y